a = int(input("Nhập số nguyên thứ nhất (a): "))
b = int(input("Nhập số nguyên thứ hai (b): "))
c = int(input("Nhập số nguyên thứ ba (c): "))
d = int(input("Nhập số nguyên thứ tư (d): "))

trung_binh_cong = (a + b + c + d) / 4

print("Trung bình cộng của 4 số là:", trung_binh_cong)